//  将 32 位整数左移 n 位
uint32_t left_rotate(uint32_t x, int n) {
    return ((x << n) & 0xFFFFFFFF) | (x >> (32 - n));
}

// S-box
uint32_t sm4_sbox(uint32_t x) {
    return (s_box[x >> 24] << 24) | (s_box[(x >> 16) & 0xFF] << 16) | (s_box[(x >> 8) & 0xFF] << 8) | s_box[x & 0xFF];
}

// 左移线性变化
uint32_t sm4_t(uint32_t x) {
    return x ^ left_rotate(x, 2) ^ left_rotate(x, 10) ^ left_rotate(x, 18) ^ left_rotate(x, 24);
}

// 8-byte 16进制字符串转化为32_bit整数
uint32_t hex_to_int(const string& hex_str) {
    return stoul(hex_str, nullptr, 16);
}

// 32-bit 整数转化为8-byte 16进制字符串
string int_to_hex(uint32_t num) {
    char buffer[9];
    snprintf(buffer, sizeof(buffer), "%08x", num);
    return string(buffer);
}

// 密钥生成
void sm4_key_schedule(const string& key_hex, uint32_t* rk) {
    uint32_t key[4];
    for (int i = 0; i < 4; ++i) {
        key[i] = hex_to_int(key_hex.substr(i * 8, 8));
        rk[i] = key[i] ^ FK[i];
    }
    for (int i = 0; i < 32; ++i) {
        uint32_t y = sm4_sbox(rk[i + 1] ^ rk[i + 2] ^ rk[i + 3] ^ CK[i]);
        rk[i + 4] = rk[i] ^ y ^ left_rotate(y, 13) ^ left_rotate(y, 23);
    }
    memmove(rk, rk + 4, 32 * sizeof(uint32_t));
}

// 加密/解密的主体函数
void sm4_encrypt_decrypt(const uint32_t* plaintext, const uint32_t* rk, uint32_t* ciphertext) {
    uint32_t x[4];
    for (int i = 0; i < 4; ++i) {
        x[i] = plaintext[i];
    }

    for (int i = 0; i < 32; ++i) {
        uint32_t tmp = x[1] ^ x[2] ^ x[3] ^ rk[i];
        tmp = sm4_sbox(tmp);
        tmp = sm4_t(tmp);
        x[0] ^= tmp;
        swap(x[0], x[1]);
        swap(x[1], x[2]);
        swap(x[2], x[3]);
    }

    for (int i = 0; i < 4; ++i) {
        ciphertext[i] = x[3 - i];
    }
}

